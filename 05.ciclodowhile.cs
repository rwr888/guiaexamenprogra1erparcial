int i=9;

// El ciclo do-while lleva a cabo las instruccinoes al menos una vez
do// mientras i sea menor a 10, se ejecutaran las instrucciones del bloque do-while
{
	Console.WriteLine(i+1);//imprime el valor de i+1 en consola
	i++;
}while(i<10); //La validación se hace hasta el final del bloque de instrucciones.
// Fin Do-while